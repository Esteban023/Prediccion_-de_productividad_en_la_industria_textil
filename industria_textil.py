def r2_score(y_true, y_pred):
    # Asegurar que las formas coincidan
    y_true = tf.reshape(y_true, [-1])
    y_pred = tf.reshape(y_pred, [-1])

    # Calcular de manera numéricamente estable
    SS_res = tf.reduce_sum(tf.square(y_true - y_pred))
    SS_tot = tf.reduce_sum(tf.square(y_true - tf.reduce_mean(y_true)))

    # Evitar división por cero y valores absurdos
    r2 = 1 - (SS_res / (SS_tot + tf.keras.backend.epsilon()))

    # Limitar el rango para evitar valores extremos
    return tf.clip_by_value(r2, -100, 100)  # Límite razonable

