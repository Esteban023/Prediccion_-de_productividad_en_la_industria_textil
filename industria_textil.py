    # Features derivadas
    df['theoretical_productivity'] = df['smv'] * df['no_of_workers']
    df['incentive_ratio'] = df['incentive'] / (df['over_time'] + 1)
    df['workload_per_worker'] = df['smv'] / (df['no_of_workers'] + 1)
    df['style_change_rate'] = df['no_of_style_change'] / (df['no_of_workers'] + 1)
    df['time_per_operation'] = df['over_time'] / (df['no_of_workers'] + 1)
    df['incentive_smv_interaction'] = df['incentive'] * df['smv']
    df['workers_overtime_interaction'] = df['no_of_workers'] * df['over_time']

    df = df.drop('date', axis=1)
    return df

