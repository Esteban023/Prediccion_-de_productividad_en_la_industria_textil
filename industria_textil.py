def plot_training_history(history, title_suffix):
    history_dict = history.history

    plt.figure(figsize=(12, 5))

    # Plot Loss
    plt.subplot(1, 2, 1)
    plt.plot(history_dict['loss'], label='Training Loss')
    plt.plot(history_dict['val_loss'], label='Validation Loss')
    plt.title(f'Loss over Epochs ({title_suffix})')
    plt.xlabel('Epochs')
    plt.ylabel('Loss (Huber)')
    plt.legend()
    plt.grid(True)

    # Plot R2 Score
    plt.subplot(1, 2, 2)
    plt.plot(history_dict['r2_score'], label='Training R2 Score')
    plt.plot(history_dict['val_r2_score'], label='Validation R2 Score')
    plt.title(f'R2 Score over Epochs ({title_suffix})')
    plt.xlabel('Epochs')
    plt.ylabel('R2 Score')
    plt.legend()
    plt.grid(True)

    plt.tight_layout()
    plt.show()

