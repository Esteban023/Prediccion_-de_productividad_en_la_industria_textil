def create_features_finishing(df):
    df = df.copy()

    # Normaliza y filtra finishing
    df["department"] = df["department"].astype(str).str.strip()
    df = df[df["department"].str.lower() != "sweing"].copy()

    # Drop columnas sin señal en finishing
    df = df.drop(["idle_time", "idle_men", "no_of_style_change", "wip", "quarter"], axis=1, errors="ignore")

    # Fecha -> ordinal
    df["date"] = pd.to_datetime(df["date"], errors="coerce")
    df = df.dropna(subset=["date"])  # por si hubiera fechas malas
    df["ordinal_date"] = df["date"].map(lambda x: x.toordinal())

    # Features derivadas (protegiendo divisiones)
    workers = df["no_of_workers"].astype(float)
    ot = df["over_time"].astype(float)
    smv = df["smv"].astype(float)
    inc = df["incentive"].astype(float)

    df["theoretical_productivity"] = smv * workers
    df["incentive_ratio"] = inc / (ot + 1.0)
    df["workload_per_worker"] = smv / (workers + 1.0)
    df["time_per_operation"] = ot / (workers + 1.0)
    df["workers_overtime_interaction"] = workers * ot

    # Si incentive casi siempre es 0 en finishing, a veces ayuda más así:
    df["has_incentive"] = (df["incentive"] > 0).astype(int)
    # Opcional: si usas has_incentive, puedes dropear incentive:
    # df = df.drop("incentive", axis=1)

    df = df.drop("date", axis=1)
    return df

